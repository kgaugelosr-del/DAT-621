---SECTION 4 - SELECT queries---
-- Exercise 21 - Basic SELECT

SELECT FIRST_NAME || ' ' || LAST_NAME AS FULL_NAME,
       DEPT_NAME,
       ENROLL_DATE
FROM STUDENTS S
JOIN DEPARTMENTS D
ON S.DEPT_ID = D.DEPT_ID;

--Exercise 22 - Filtering--

SELECT * FROM STUDENTS
WHERE FEES > 1000;

-- Exercise 23 - Sorting--

SELECT * FROM STUDENTS
ORDER BY LAST_NAME;

-- Exercise 24 - INNER JOIN--

SELECT S.FIRST_NAME,
       S.LAST_NAME,
       D.DEPT_NAME
FROM STUDENTS S
INNER JOIN DEPARTMENTS D
ON S.DEPT_ID = D.DEPT_ID;

-- Exercise 25 - GROUP by--

SELECT D.DEPT_NAME,
       COUNT(*) AS TOTAL_STUDENTS
FROM STUDENTS S
JOIN DEPARTMENTS D
ON S.DEPT_ID = D.DEPT_ID
GROUP BY D.DEPT_NAME;

-- Exercise 26 - Aggregate Functions--

SELECT
SUM(FEES) AS TOTAL_FEES,
AVG(FEES) AS AVERAGE_FEES,
MAX(FEES) AS HIGHEST_FEES,
MIN(FEES) AS LOWEST_FEES
FROM STUDENTS;

