-- Exercise 38 - Create a VIEW--

CREATE VIEW VW_STUDENT_DETAILS AS
SELECT
S.FIRST_NAME || ' ' || S.LAST_NAME AS FULL_NAME,
D.DEPT_NAME,
S.FEES
FROM STUDENTS S
JOIN DEPARTMENTS D
ON S.DEPT_ID = D.DEPT_ID;

--Exercise 39 - Inline View--

SELECT *
FROM
(
SELECT FIRST_NAME, LAST_NAME, FEES
FROM STUDENTS
ORDER BY FEES DESC
)
WHERE ROWNUM <= 3;

