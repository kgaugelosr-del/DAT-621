-- Exercise 34 - CASE Expression--

SELECT FIRST_NAME,
FEES,
CASE
WHEN FEES > 5000 THEN 'HIGH'
WHEN FEES BETWEEN 2000 AND 5000 THEN 'MEDIUM'
ELSE 'LOW'
END AS FEE_CATEGORY
FROM STUDENTS;

-- Exercise 35 - HAVING--

SELECT DEPT_ID,
COUNT(*) AS TOTAL_STUDENTS
FROM STUDENTS
GROUP BY DEPT_ID
HAVING COUNT(*) > 3;

