(
SELECT AVG(FEES)
FROM STUDENTS
);

-- Exercise 32 - EXITS--

SELECT *
FROM DEPARTMENTS D
WHERE NOT EXISTS
(
SELECT *
FROM STUDENTS S
WHERE S.DEPT_ID = D.DEPT_ID
);

-- Exercise 33 - UNION--

SELECT DEPT_NAME FROM DEPARTMENTS
